    public void resetForm(){
        txtHoTen.setText("");
        txtNamSinh.setText("");
        txtDiaChi.setText("");
        txtHeSoLuong.setText("");
        txtNganhDaoTao.setText("");
        txtHeSoChucVu.setText("");
        cbbGioiTinh.setSelectedIndex(-1);
        buttonGroup.clearSelection();
    }
    
    private void btnLuuActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnLuuActionPerformed
        // TODO add your handling code here:
        if(jbrNhanVien.isSelected()){
            NhanVien nv = new NhanVien();
            
            nv.setHoTen(txtHoTen.getText());
            if(Double.parseDouble(txtNamSinh.getText()) > 2025 || Double.parseDouble(txtNamSinh.getText()) < 1900){
                JOptionPane.showMessageDialog(rootPane, "Nam Sinh Khong Hop Le! Vui Long Thu Lai");
                return;
            }
            nv.setNamSinh(txtNamSinh.getText());
            nv.setGioiTinh(cbbGioiTinh.getSelectedItem().toString());
            nv.setDiaChi(txtDiaChi.getText());
            nv.setHeSoLuong(Double.parseDouble(txtHeSoLuong.getText()));
            nv.setNganhDaoTao(txtNganhDaoTao.getText());
            
            danhSachCanBo.add(nv);
            JOptionPane.showMessageDialog(rootPane, "Them Nhan Vien Thanh Cong!");
            resetForm();
        }
        else if(jbrQuanLy.isSelected()){
            QuanLy ql = new QuanLy();
            
            ql.setHoTen(txtHoTen.getText());
            if(Double.parseDouble(txtNamSinh.getText()) > 2025 || Double.parseDouble(txtNamSinh.getText()) < 1900){
                JOptionPane.showMessageDialog(rootPane, "Nam Sinh Khong Hop Le! Vui Long Thu Lai");
                return;
            }
            ql.setNamSinh(txtNamSinh.getText());
            ql.setGioiTinh(cbbGioiTinh.getSelectedItem().toString());
            ql.setDiaChi(txtDiaChi.getText());
            ql.setHeSoLuong(Double.parseDouble(txtHeSoLuong.getText()));
            if(Double.parseDouble(txtHeSoChucVu.getText()) > 1 || Double.parseDouble(txtHeSoChucVu.getText()) < 0){
                JOptionPane.showMessageDialog(rootPane, "He So Chuc Vu Khong Hop Le! Vui Long Thu Lai");
                return;
            }
            ql.setHeSoChucVu(Double.parseDouble(txtHeSoChucVu.getText()));
            
            danhSachCanBo.add(ql);
            JOptionPane.showMessageDialog(rootPane, "Them Quan Ly Thanh cong!");
            resetForm();
        }
    }//GEN-LAST:event_btnLuuActionPerformed

    private void btnLuuFileActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnLuuFileActionPerformed
        // TODO add your handling code here:
        try{
            File file = new File("canboDe1_1.txt");
            
            PrintWriter pw = new PrintWriter(file);
            
            if(danhSachCanBo.isEmpty()){
                JOptionPane.showMessageDialog(rootPane, "Danh Sach Can Bo Trong! Khong The Luu File");
                return;
            }
            
            for(CanBo cb : danhSachCanBo){
                pw.println(cb.toString());
            }
            pw.close();
            JOptionPane.showMessageDialog(rootPane, "Luu File Thanh Cong!");
        }catch(FileNotFoundException e){
            System.out.println("Loi Khi Mo File!" + e.getMessage());
        }
    }//GEN-LAST:event_btnLuuFileActionPerformed

    private void btnHienThiActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnHienThiActionPerformed
        // TODO add your handling code here:
        for(CanBo cb : danhSachCanBo){
            if(cb instanceof NhanVien nv){
                Object[] row = new Object[]{
                    nv.getHoTen(), nv.getNamSinh(), nv.getGioiTinh(), nv.getDiaChi(), nv.getHeSoLuong(), nv.LUONG_CO_BAN, "NganhDaoTao: " + nv.getNganhDaoTao(), nv.tinhLuong()
                };
                tableModel.addRow(row);
                tableModel.fireTableDataChanged();
            }
            else if(cb instanceof QuanLy ql){
                Object[] row = new Object[]{
                    ql.getHoTen(), ql.getNamSinh(), ql.getGioiTinh(), ql.getDiaChi(), ql.getHeSoLuong(), ql.LUONG_CO_BAN, "HeSoChucVu:" + ql.getHeSoChucVu(), ql.tinhLuong()
                };
                tableModel.addRow(row);
                tableModel.fireTableDataChanged();
            }
        }
    }//GEN-LAST:event_btnHienThiActionPerformed

