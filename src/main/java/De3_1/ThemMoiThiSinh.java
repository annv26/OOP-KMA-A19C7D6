    private void btnLuuActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnLuuActionPerformed
        // TODO add your handling code here:        
        if(rbKhoiA.isSelected()){
            ThiSinhKhoiA tsA = new ThiSinhKhoiA();
            
            tsA.setHoTen(txtHoTen.getText());
            tsA.setSoBaoDanh(txtSoBaoDanh.getText());
            tsA.setTruongHoc(txtTruongHoc.getText());
            tsA.setDiemCong(Double.parseDouble(txtDiemCong.getText()));
            tsA.setDiemToan(Double.parseDouble(txtDiemToanA.getText()));
            tsA.setDiemLy(Double.parseDouble(txtDiemLyA.getText()));
            tsA.setDiemHoa(Double.parseDouble(txtDiemHoaA.getText()));
            
            danhSachThiSinh.add(tsA);
            JOptionPane.showMessageDialog(rootPane, "Them Thi Sinh Khoi A Thanh Cong!");
            resetForm();
        }
        else if(rbKhoiD.isSelected()){
            ThiSinhKhoiD tsD = new ThiSinhKhoiD();
            
            tsD.setHoTen(txtHoTen.getText());
            tsD.setSoBaoDanh(txtSoBaoDanh.getText());
            tsD.setTruongHoc(txtTruongHoc.getText());
            tsD.setDiemCong(Double.parseDouble(txtDiemCong.getText()));
            tsD.setDiemToan(Double.parseDouble(txtDiemToanD.getText()));
            tsD.setDiemVan(Double.parseDouble(txtDiemVanD.getText()));
            tsD.setDiemAnh(Double.parseDouble(txtDiemAnhD.getText()));
            
            danhSachThiSinh.add(tsD);
            JOptionPane.showMessageDialog(rootPane, "Them Thi Sinh Khoi D Thanh Cong!");
            resetForm();
        }
        else{
            JOptionPane.showMessageDialog(rootPane, "Vui Long Chon Loai Thi Sinh!");
        }
    }//GEN-LAST:event_btnLuuActionPerformed

    private void btnLuuFileActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnLuuFileActionPerformed
        // TODO add your handling code here:
        try{
            File file = new File("thisinhDe3_1.txt");
            
            PrintWriter pw = new PrintWriter(file);
            if(danhSachThiSinh.isEmpty()){
                JOptionPane.showMessageDialog(rootPane, "Danh Sach Thi Sinh Trong!");
                return;
            }
            for(ThiSinh ts : danhSachThiSinh){
                pw.println(ts.toString());
            }
            pw.close();
            JOptionPane.showMessageDialog(rootPane, "Ghi File Thanh Cong!");
        }
        catch(FileNotFoundException e){
            System.out.println("Loi Khi Mo File" + e.getMessage());
        }
    }//GEN-LAST:event_btnLuuFileActionPerformed
    
