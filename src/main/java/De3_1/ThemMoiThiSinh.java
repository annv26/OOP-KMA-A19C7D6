    private void btnThiSinhKhoiDActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnThiSinhKhoiDActionPerformed
        // TODO add your handling code here:
        xoaDuLieuCu();
        
        boolean isChecked = false;
        int count = 0;
        
        for(ThiSinh ts : danhSachThiSinh){
            if(ts instanceof ThiSinhKhoiD){
                isChecked = true;
                ++count;
                Object[] row = new Object[]{
                    ts.getHoTen(), ts.getSoBaoDanh(), ts.getTruongHoc(), ts.getDiemCong(), ((ThiSinhKhoiD) ts).DIEM_SAN, "Khoi D", ((ThiSinhKhoiD) ts).getDiemToan(), 
                    ((ThiSinhKhoiD) ts).getDiemVan(), ((ThiSinhKhoiD) ts).getDiemAnh(), ((ThiSinhKhoiD) ts).tinhDiem()
                };
                tableModel.addRow(row);
            }
        }
        tableModel.fireTableDataChanged();
        if(isChecked == false){
            JOptionPane.showMessageDialog(rootPane, "Khong Co Thi Sinh Khoi D!");
        }
        if(count != 0){
            JOptionPane.showMessageDialog(rootPane, "Co " + count + " Thi Sinh Khoi D!");
        }
    }//GEN-LAST:event_btnThiSinhKhoiDActionPerformed

