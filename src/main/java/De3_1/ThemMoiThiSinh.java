    private void btnThapHonDiemSanActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnThapHonDiemSanActionPerformed
        // TODO add your handling code here:
        xoaDuLieuCu();
        
        boolean isChecked = false;
        int count = 0;
        
        for(ThiSinh ts : danhSachThiSinh){
            if(ts instanceof ThiSinhKhoiA){
                if(((ThiSinhKhoiA) ts).tinhDiem() < 3){
                    isChecked = true;
                    ++count;
                    Object[] row = new Object[]{
                        ts.getHoTen(), ts.getSoBaoDanh(), ts.getTruongHoc(), ts.getDiemCong(), ((ThiSinhKhoiA) ts).DIEM_SAN, "Khoi A", ((ThiSinhKhoiA) ts).getDiemToan(), 
                        ((ThiSinhKhoiA) ts).getDiemLy(), ((ThiSinhKhoiA) ts).getDiemHoa(), ((ThiSinhKhoiA) ts).tinhDiem()
                    };
                    tableModel.addRow(row);
                }
            }
            else if(ts instanceof ThiSinhKhoiD){
                if(((ThiSinhKhoiD) ts).tinhDiem() < 3){
                    isChecked = true;
                    ++count;
                    Object[] row = new Object[]{
                        ts.getHoTen(), ts.getSoBaoDanh(), ts.getTruongHoc(), ts.getDiemCong(), ((ThiSinhKhoiD) ts).DIEM_SAN, "Khoi D", ((ThiSinhKhoiD) ts).getDiemToan(), 
                        ((ThiSinhKhoiD) ts).getDiemVan(), ((ThiSinhKhoiD) ts).getDiemAnh(), ((ThiSinhKhoiD) ts).tinhDiem()
                    };
                    tableModel.addRow(row);
                }
            }
        }
        tableModel.fireTableDataChanged();
        if(isChecked == false){
            JOptionPane.showMessageDialog(rootPane, "Khong Co Thi Sinh Nao Thap Hon Diem San!");
        }
        if(count != 0){
            JOptionPane.showMessageDialog(rootPane, "Co " + count + " Thi Sinh Thap Hon Diem San!");
        }
    }//GEN-LAST:event_btnThapHonDiemSanActionPerformed

