    private void btnLuuFileActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnLuuFileActionPerformed
        // TODO add your handling code here:
        try{
            File file = new File("nhanvienDe1_2.txt");
            
            PrintWriter pw = new PrintWriter(file);
            if(danhSachNhanVien.isEmpty()){
                JOptionPane.showMessageDialog(rootPane, "Danh Sach Nhan Vien Trong!");
                return;
            }
            for(NhanVien nv : danhSachNhanVien){
                pw.println(nv.toString());
            }
            pw.close();
            JOptionPane.showMessageDialog(rootPane, "Ghi File Thanh Cong!");
        }catch(FileNotFoundException e){
            System.out.println("Loi Khi Mo File" + e.getMessage());
        }
    }//GEN-LAST:event_btnLuuFileActionPerformed

    private void btnHienThiActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnHienThiActionPerformed
        // TODO add your handling code here:
        for(NhanVien nv : danhSachNhanVien){
            if(nv instanceof NhanVienBep nvb){
                Object[] row = new Object[]{
                    nvb.getHoTen(), nvb.getThamNien(), nvb.getQueQuan(), nvb.getSoGioLamViec(), nvb.LUONG_GIO
                        , nvb.isIsBepTruong(), nvb.getThuongChucVu(), null, nvb.tinhLuong()
                };
                tableModel.addRow(row);
                tableModel.fireTableDataChanged();
            }
            else if(nv instanceof NhanVienThuNgan nvtn){
                Object[] row = new Object[]{
                    nvtn.getHoTen(), nvtn.getThamNien(), nvtn.getQueQuan(), nvtn.getSoGioLamViec(), nvtn.LUONG_GIO
                        ,null, null, nvtn.isIsBietNgoaiNgu(), nvtn.tinhLuong()
                };
                tableModel.addRow(row);
                tableModel.fireTableDataChanged();
            }
        }
    }//GEN-LAST:event_btnHienThiActionPerformed

