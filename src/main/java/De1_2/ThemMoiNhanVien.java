    private void btnLuuActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnLuuActionPerformed
        // TODO add your handling code here:
        if(rbNhanVienBep.isSelected()){
            NhanVienBep nvb = new NhanVienBep();
            
            nvb.setHoTen(txtHoTen.getText());
            nvb.setThamNien(Integer.parseInt(txtThamNien.getText()));
            nvb.setQueQuan(txtQueQuan.getText());
            nvb.setSoGioLamViec(Float.parseFloat(txtSoGioLamViec.getText()));
            if(rbNhanVienBepKhong.isSelected()){
                nvb.setIsBepTruong(false);
                nvb.setThuongChucVu(0);
                txtThuongChucVu.setEditable(false);
            }
            else if(rbNhanVienBepCo.isSelected()){
                nvb.setIsBepTruong(true);
                txtThuongChucVu.setEditable(true);
                nvb.setThuongChucVu(Integer.parseInt(txtThuongChucVu.getText()));
            }
            danhSachNhanVien.add(nvb);
            JOptionPane.showMessageDialog(rootPane, "Them Nhan Vien Bep Thanh Cong!");
            resetForm();
        }
        else if(rbNhanVienThuNgan.isSelected()){
            NhanVienThuNgan nvtn = new NhanVienThuNgan();
            
            nvtn.setHoTen(txtHoTen.getText());
            nvtn.setThamNien(Integer.parseInt(txtThamNien.getText()));
            nvtn.setQueQuan(txtQueQuan.getText());
            nvtn.setSoGioLamViec(Float.parseFloat(txtSoGioLamViec.getText()));
            if(rbNhanVienThuNganCo.isSelected()){
                nvtn.setIsBietNgoaiNgu(true);
            }
            else if(rbNhanVienThuNganKhong.isSelected()){
                nvtn.setIsBietNgoaiNgu(false);
            }
            nvtn.tinhLuong();
            danhSachNhanVien.add(nvtn);
            JOptionPane.showMessageDialog(rootPane, "Them Nhan Vien Thu Ngan Thanh Cong!");
            resetForm();
        }
        else{
            JOptionPane.showMessageDialog(rootPane, "Ban Chua Chon Chuc Vu!");
        }
    }//GEN-LAST:event_btnLuuActionPerformed

